#Assignment Operators:
#Assignment oprators are include addition assignment, subtraction assinnment, multiplication assignment,
#division assignment, reminder assignment, exponent assignment etc.

number1= int(input("enter first number:"))
number2= int(input("enter second number"))

#addition assignment (+=)
number2+=number1
print(number2)

#subtraction assinnment (-=)
number1-=number2
print(number1)

#multiplication assignment(*=)
number1*=number2
print(number1)

#division assignment(/=)
number1/=number2
print(number1)

# reminder assignment(%=)
number1%=number2
print(number1)

#exponent assignment(**=)
number1**=number2
print(number1)
